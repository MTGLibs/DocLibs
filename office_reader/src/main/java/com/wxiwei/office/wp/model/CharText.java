package com.wxiwei.office.wp.model;

public class CharText {
    public String text;
    public float x;
    public float y;
    public float zoom;

    public CharText(String text, float x, float y, float zoom) {
        this.text = text;
        this.x = x;
        this.y = y;
        this.zoom = zoom;
    }
}
